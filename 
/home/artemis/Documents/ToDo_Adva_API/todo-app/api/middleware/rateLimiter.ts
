
import rateLimit from 'express-rate-limit';

// Custom keyGenerator that handles proxy headers properly
const createKeyGenerator = () => {
  return (req: any) => {
    // If trust proxy is enabled, use req.ip which Express will derive from x-forwarded-for
    // If not, fall back to a simple approach
    if (req.app.get('trust proxy')) {
      return req.ip || req.socket.remoteAddress || 'unknown';
    }
    // Fallback: use a combination of headers (less accurate but won't throw)
    return req.headers['x-forwarded-for']?.split(',')[0]?.trim() || 
           req.socket.remoteAddress || 
           'unknown';
  };
};

// General API rate limiter
export const apiLimiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 100, // limit each IP to 100 requests per windowMs
  message: 'Too many requests from this IP, please try again later.',
  standardHeaders: true,
  legacyHeaders: false,
  keyGenerator: createKeyGenerator(),
  validate: false, // Disable all validations to avoid x-forwarded-for issues
});

// Login attempt limiter - strict
export const loginLimiter = rateLimit({
  windowMs: 60 * 1000, // 1 minute
  max: 5, // max 5 login attempts per minute
  message: 'Too many login attempts, please try again later.',
  skipSuccessfulRequests: true, // don't count successful logins
  standardHeaders: true,
  legacyHeaders: false,
  keyGenerator: createKeyGenerator(),
  validate: false,
});

// Registration limiter
export const registerLimiter = rateLimit({
  windowMs: 60 * 60 * 1000, // 1 hour
  max: 5, // max 5 registrations per hour per IP
  message: 'Too many registration attempts, please try again later.',
  standardHeaders: true,
  legacyHeaders: false,
  keyGenerator: createKeyGenerator(),
  validate: false,
});

// Password reset limiter
export const passwordResetLimiter = rateLimit({
  windowMs: 60 * 60 * 1000, // 1 hour
  max: 3, // max 3 password reset requests per hour
  message: 'Too many password reset attempts, please try again later.',
  standardHeaders: true,
  legacyHeaders: false,
  keyGenerator: createKeyGenerator(),
  validate: false,
});

